function year1612() {
    document.body.style.backgroundImage = "url('Images/space.jpg')";
    let element = document.getElementById("parallax");
    element.style.backgroundImage = "url('Images/space.jpg')";
    element.style.backgroundRepeat = "no-repeat";
    element.style.backgroundPosition = "center";
    document.getElementById('response-header-1').innerText = 'DECEMBER 28, 1612'
    document.getElementById('response-header-2').innerText = 'Galileo observes Neptune'
    document.getElementById('response-text-1').innerText = 'Neptune is too dim to be visible to the naked eye: its apparent magnitude is never brighter than 7.7. Therefore, the first observations of Neptune were only possible after the invention of the telescope. There is evidence that Neptune was seen and recorded by Galileo Galilei in 1613, but he was not known to have recognized it as a planet at the time. These pre-discovery observations were important in accurately determining the orbit of Neptune. Neptune would appear prominently even in early telescopes so other pre-discovery observation records are likely.'
    document.getElementById('response-img-1').src = 'Images/galileo.jpg'
    document.getElementById('response-text-2').innerText = 'Galileos drawings show that he observed Neptune on December 28, 1612, and again on January 27, 1613; on both occasions, Galileo mistook Neptune for a fixed star when it appeared very close (in conjunction) to Jupiter in the night sky. Historically it was thought that he believed it to be a fixed blue star, and so he is not credited with its discovery. At the time of his first observation in December 1612, it was stationary in the sky because it had just turned retrograde that very day; because it was only beginning its yearly retrograde cycle, Neptunes motion was thought to be too slight, and its apparent size too small, to clearly appear to be a planet in Galileos small telescope.'
    document.getElementById('response-img-2').src = 'Images/neptune.jpg'
    document.getElementById('response-img-3').src = 'Images/galileotelescope.jpg'
}

